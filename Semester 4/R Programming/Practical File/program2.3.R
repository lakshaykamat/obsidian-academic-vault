# Q2.3 WAP to create an 3D array of 24 elements using the DIM() funtions

# Set the dimensions of the 3D array to have 24 elements
dims <- c(2, 3, 4)  # Dimensions: 2 rows, 3 columns, 4 layers 

# Create a 3D array with specified dimensions
array_data <- array(1:24, dim = dims)
# Print the 3D array
print(array_data)
 