# Numeric vector
numeric_vector <- c(1, 2, 3, 4, 5, 6)

# Complex vector
complex_vector <- c(1 + 2i, 3 + 4i, 5 + 6i, 7 + 8i, 9 + 10i, 11 + 12i)

# Logical vector
logical_vector <- c(TRUE, FALSE, TRUE, FALSE, TRUE, FALSE)

# Character vector
character_vector <- c("apple", "banana", "orange", "grape", "watermelon", "pineapple")

# Print vectors
print("Numeric vector:")
print(numeric_vector)

print("Complex vector:")
print(complex_vector)

print("Logical vector:")
print(logical_vector)

print("Character vector:")
print(character_vector)
